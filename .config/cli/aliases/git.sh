alias secretfiles='/usr/bin/git --git-dir=$HOME/.secretfiles --work-tree=$HOME'

alias gs='git status'
alias ga='git add'
alias gc='git commit'
alias gp='git push'
alias gl='git log'
alias gd='git diff'
alias gds='git diff --staged'
